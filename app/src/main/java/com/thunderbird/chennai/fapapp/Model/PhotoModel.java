public class PhotoModel {

    String userPhotoId;
    String userPhoto;
    String userPhotoCreatedDate;

    public String getUserPhotoId() {
        return userPhotoId;
    }

    public void setUserPhotoId(String userPhotoId) {
        this.userPhotoId = userPhotoId;
    }

    public String getUserPhoto() {
        return userPhoto;
    }

    public void setUserPhoto(String userPhoto) {
        this.userPhoto = userPhoto;
    }

    public String getUserPhotoCreatedDate() {
        return userPhotoCreatedDate;
    }

    public void setUserPhotoCreatedDate(String userPhotoCreatedDate) {
        this.userPhotoCreatedDate = userPhotoCreatedDate;
    }
}
