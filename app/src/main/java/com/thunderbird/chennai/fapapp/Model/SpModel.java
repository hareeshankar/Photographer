public class SpModel {

    String spId;
    String spName;
    String icon;
    String spStatus;
    String spCreatedDate;
    String userhave;

    public String getUserhave() {
        return userhave;
    }

    public void setUserhave(String userhave) {
        this.userhave = userhave;
    }

    public String getSpId() {
        return spId;
    }

    public void setSpId(String spId) {
        this.spId = spId;
    }

    public String getSpName() {
        return spName;
    }

    public void setSpName(String spName) {
        this.spName = spName;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getSpStatus() {
        return spStatus;
    }

    public void setSpStatus(String spStatus) {
        this.spStatus = spStatus;
    }

    public String getSpCreatedDate() {
        return spCreatedDate;
    }

    public void setSpCreatedDate(String spCreatedDate) {
        this.spCreatedDate = spCreatedDate;
    }
}
